def normalize_language(lang: str | None) -> str:
    raw = str(lang or DEFAULT_LANGUAGE).lower().strip()
    return raw if raw in AVAILABLE_LANGUAGES else DEFAULT_LANGUAGE


def t(key: str, lang: str | None = None, **kwargs: Any) -> str:
    language = normalize_language(lang)
    value = (
        TRANSLATIONS.get(language, {}).get(key)
        or TRANSLATIONS[DEFAULT_LANGUAGE].get(key)
        or key
    )
    if kwargs:
        try:
            return value.format(**kwargs)
        except Exception:
            return value
    return value


def get_report_i18n(lang: str | None = None) -> dict[str, str]:
    language = normalize_language(lang)
    keys = set(TRANSLATIONS[DEFAULT_LANGUAGE].keys()) | set(TRANSLATIONS.get(language, {}).keys())
    payload = {key: t(key, language) for key in keys}
    payload["report.html_lang"] = language
    return payload


def month_label(month_code: str, lang: str | None = None) -> str:
    key = MONTH_KEY_BY_CODE.get(str(month_code), "")
    if not key:
        return str(month_code)
    return t(key, lang)


def month_labels(lang: str | None = None) -> list[str]:
    return [month_label(code, lang) for code in MONTH_KEY_BY_CODE]
